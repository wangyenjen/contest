ll fact[13];

ll phi(ll x) {
  ll t = x;
  for (ll i = 2; i * i <= x; i++) {
    if (t % i == 0) {
      x = x / i * (i - 1);
      while (t % i == 0) t /= i;
    }
  }
  if (t > 1) x = x / t * (t - 1);
  return x;
}

ll power(ll a, ll n, ll m) {
  ll x = 1 % m;
  while (n) {
    if (n & 1) x = x * a % m;
    a = a * a % m;
    n >>= 1;
  }
  return x;
}

ll dfs(ll a, ll m) {
  if (m == 1) return 0;
  else {
    ll k = phi(m);
    return power(a, k + dfs(a, k), m);
  }
}

